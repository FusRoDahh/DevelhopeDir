public class Funzioni_1 {
    public static void main(String[] args) {
        int firstNum = 5;
        int secondNum = 7;

        System.out.println("I due numeri sommati sono uguali a: "
                + sum(firstNum, secondNum));
    }

    public static int sum (int x, int y) {
        return x + y;
    }
}
